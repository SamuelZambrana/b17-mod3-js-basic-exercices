// Typeof variable is number

const number = 1;

if (number > 0) {
  console.log(`El numero ${number} es: positivo`)
} else if (number = 0) {
  console.log(`El numero ${number} es: igual a 0`)
} else {
  console.log(`El numero ${number} es: negativo`)
}